import { readFileSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const pkgRoot = join(__dirname, '..');
const pkg = JSON.parse(readFileSync(join(pkgRoot, 'package.json'), 'utf8'));
const genDir = join(pkgRoot, 'src', 'generated');
mkdirSync(genDir, { recursive: true });
const outPath = join(genDir, 'faroRNPackageMeta.ts');

/** Single-quoted TS string literal (matches Prettier output for this repo). */
function tsSingleQuoted(value) {
  return `'${String(value).replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

const content = `/**
 * NPM package identity for this build. Generated by scripts/sync-faro-rn-package-meta.mjs - do not edit.
 */
export const FARO_REACT_NATIVE_NPM_NAME = ${tsSingleQuoted(pkg.name)} as const;
export const FARO_REACT_NATIVE_NPM_VERSION = ${tsSingleQuoted(pkg.version)} as const;
`;
writeFileSync(outPath, content);
